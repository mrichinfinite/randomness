class Arithmetic:
    '''
    Define various functions that perform different 
    arithmetic operations and then return the result.
    '''
    def addition(a, b):
        '''
        Add two numbers and return the result.
        '''
        result = float(a) + float(b)
        return result

    def subtraction(a, b):
        '''
        Subtract two numbers and return the result.
        '''
        result = float(a) - float(b)
        return result

    def multiplication(a, b):
        '''
        Multiply two numbers and return the result.
        '''
        result = float(a) * float(b)
        return result
        
    def division(a, b):
        '''
        Divide two numbers and return the result.
        '''
        result = float(a) / float(b)
        return result